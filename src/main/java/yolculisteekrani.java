    private void yolcu_tablosuMouseClicked(java.awt.event.MouseEvent evt) {                                           
        // TODO add your handling code here:
        int selectedrow=yolcu_tablosu.getSelectedRow();
        adAlani.setText(model.getValueAt(selectedrow,0).toString()); 
        soyadAlani.setText(model.getValueAt(selectedrow,1).toString());
        guzergahAlani.setSelectedItem(model.getValueAt(selectedrow,2).toString());
        plakaAlani.setSelectedItem(model.getValueAt(selectedrow,3).toString());
        gunAlani.setSelectedItem(model.getValueAt(selectedrow,4).toString());
        saatAlani.setSelectedItem(model.getValueAt(selectedrow,5).toString());
        koltukAlani.setSelectedItem(model.getValueAt(selectedrow,6).toString());
        kaptaninAdiAlani.setSelectedItem(model.getValueAt(selectedrow,7).toString());
        
    }                                          

    private void guncelleActionPerformed(java.awt.event.ActionEvent evt) {                                         
        // TODO add your handling code here:
        String ad=adAlani.getText();
        String soyad=soyadAlani.getText();
        String guzergah=guzergahAlani.getSelectedItem().toString();
        String plaka=plakaAlani.getSelectedItem().toString();
        String gun=gunAlani.getSelectedItem().toString();
        String saat=saatAlani.getSelectedItem().toString();
        String koltuk=koltukAlani.getSelectedItem().toString();
        String kaptanin_adi=kaptaninAdiAlani.getSelectedItem().toString();
        
        
        int selectedrow=yolcu_tablosu.getSelectedRow();
        if(selectedrow==-1){
            if(model.getRowCount()==0){
                mesaj_yazisi.setText("yolcular tablosu su anda bos");
            }
            else{
                mesaj_yazisi.setText("Lutfen guncellenecek bir yolcu secin.");
            }
        }
        else{
            int yolcu_numarasi=(int)model.getValueAt(selectedrow,8);
            islemler.yolcuGuncelle(ad,soyad,guzergah,plaka,gun,saat,koltuk,kaptanin_adi,yolcu_numarasi);
            YolcuGoruntule();
            mesaj_yazisi.setText("yolcuBasariylaGuncellendi...");
        }
    }                                        

    private void silActionPerformed(java.awt.event.ActionEvent evt) {                                    
        mesaj_yazisi.setText("");
        int selectedrow=yolcu_tablosu.getSelectedRow();
        if(selectedrow==-1){
            if(model.getRowCount()==0){
                
            
             mesaj_yazisi.setText("yolcu tablosu su anda bos...");
            }
        else{
            mesaj_yazisi.setText("Lutfen silinecek bir yolcu secin...");    
        
     }}
        else{
                int yolcu_numarasi=(int)model.getValueAt(selectedrow,8);
                
                islemler.yolcuSil(yolcu_numarasi);
                YolcuGoruntule();
                mesaj_yazisi.setText("Yolcu basariyla silindi...");
                }
    }                                   
   
