    public void YolcuGoruntule(){
        model.setRowCount(0);
        ArrayList<yolcubilgi>yolcular=new ArrayList<yolcubilgi>();
        yolcular=islemler.yolculariGetir();
        if(yolcular!=null){
            for(yolcubilgi yolcu:yolcular){
                Object[] eklenecek={yolcu.getAd(),yolcu.getSoyad(),yolcu.getGuzergah(),yolcu.getPlaka(),yolcu.getGun(),yolcu.getSaat(),yolcu.getKoltuk(),yolcu.getKaptanin_adi(),yolcu.getYolcu_numarasi()};
               model.addRow(eklenecek); 
            }}
        }
